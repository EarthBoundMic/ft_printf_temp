#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	int q;

	q = 0;
	if (s != NULL)
	{
		while (s[q] != '\0')
		{
			ft_putchar_fd(s[q], fd);
			q++;
		}
	}
}
