#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		size;
	char	*str;

	size = ft_strlen(s1);
	if (!(str = (char *)malloc(sizeof(*str) * (size + 1))))
		return (NULL);
	i = 0;
	while (i < size && s1[i] != '\0')
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
