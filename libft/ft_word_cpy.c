#include "libft.h"

void	ft_word_cpy(char *d, const char *s, char c)
{
	size_t i;

	i = 0;
	while (s[i] && s[i] != c)
	{
		d[i] = s[i];
		i++;
	}
	d[i] = '\0';
}
